package com.JiCode.ProductMa.exception.requirement;

public class SwitchVersionException extends Exception {

    public SwitchVersionException() {
        super();
    }

    public SwitchVersionException(String message) {
        super(message);
    }

    public SwitchVersionException(String message, Throwable cause) {
        super(message, cause);
    }

    public SwitchVersionException(Throwable cause) {
        super(cause);
    }

}